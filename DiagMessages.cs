        public void Lookup()
        {
            string diagMessageSubString_1 = DiagNumber.Substring(3, 5);
            string diagMessageClassIdentifier = diagMessageSubString_1.Substring(0, 1);
            // Diagnostic Messages
            if (diagMessageClassIdentifier == "A")
            {
                caseSwitch = 1;
                DiagLookUpTable();
            }
            // Error Messages
            if (diagMessageClassIdentifier == "F")
            {
                caseSwitch = 2;
                DiagLookUpTable();
            }
            // Warning Messages
            if (diagMessageClassIdentifier == "E")
            {
                caseSwitch = 3;
                DiagLookUpTable();
            }
            //Command Messages
            if (diagMessageClassIdentifier == "C")
            {
                caseSwitch = 4;
                DiagLookUpTable();
            }
        }
        

